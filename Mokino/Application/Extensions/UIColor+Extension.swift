import UIKit

extension UIColor {
    
    static var customGreen: UIColor {
        UIColor(named: "CustomGreen") ?? .systemGreen
    }
    
    static var customPurple: UIColor {
        UIColor(named: "CustomPurple") ?? .purple
    }
    
    static var customBeige: UIColor {
        UIColor(named: "CustomBeige") ?? .purple
    }
    
    static var customDarkerGrey: UIColor {
        UIColor(named: "CustomDarkerGrey") ?? .purple
    }
    
}
